#include<iostream>
using namespace std;
int main()
{
	int a = 0, b = 1, c = 2, d = 3, e = 4, f = 5;
	cout << "integer	" << "square	" << "cube	" << endl;
	cout << a << "	" << a * a << "	" << a * a*a << endl;
	cout << b << "	" << b * b << "	" << b * b*b << endl;
	cout << c << "	" << c * c << "	" << c * c*c << endl;
	cout << d << "	" << d * d << "	" << d * d*d << endl;
	cout << e << "	" << e * e << "	" << e * e*e << endl;
	cout << f << "	" << f * f << "	" << f * f*f << endl;

	return 0;
}